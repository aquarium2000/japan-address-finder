# マップデータの結合
#
cat map/temp/all2_16 map/temp/all2_21 | uniq > map/temp/all3.csv
rm map/temp/all2_21
rm map/temp/all2_16
#
# 座標位置の抽出
#
echo "" > map/temp/tmp00
cat map/temp/all3.csv map/temp/tmp00 | awk '{if (a==0) {xmin=9999;ymin=9999;} if ($1=="") print xmin,xmax,ymin,ymax; else {if (xmin>$2) xmin=$2;if (xmax<$2) xmax=$2;if (ymin>$3) ymin=$3;if(ymax<$3) ymax=$3;};a++;}' > map/temp/tmp01
cat map/temp/all3.csv | awk '{print int($2),int($3);}' | sort | uniq > map/temp/tmp02
cat map/temp/all3.csv | awk '{print int($2*10),int($3*10);}' | sort | uniq > map/temp/tmp02a
cat map/temp/all3.csv | awk '{print int($2*100),int($3*100);}' | sort | uniq > map/temp/tmp02b
cat map/temp/all3.csv | awk '{print int($2)"_"int($3),int($2*10)"_"int($3*10),int($2*100)"_"int($3*100);}' | sort | uniq > map/temp/tmp02c
#
# 座標ごとのデータ分類
#
mkdir map/map_temp
cat map/temp/tmp02c | awk '{print $1;}' | sort | uniq | awk '{print "mkdir map/map_temp/"$1;}' > map/temp/tmp.macro0
source map/temp/tmp.macro0
cat map/temp/tmp02c | awk '{print $1,$2;}' | sort | uniq | awk '{print "mkdir map/map_temp/"$1"/"$2;}' > map/temp/tmp.macro1
source map/temp/tmp.macro1
cat map/temp/tmp02c | awk '{print $1,$2,$3;}' | sort | uniq | awk '{print "echo -n > map/map_temp/"$1"/"$2"/"$3;}' > map/temp/tmp.macro2
source map/temp/tmp.macro2
cat map/temp/all3.csv | awk '{print "echo XXX"$2,$3,$1"XXX >> map/map_temp/"int($2)"_"int($3)"/"int($2*10)"_"int($3*10)"/"int($2*100)"_"int($3*100);}' | sed 's/XXX/"/g' > map/temp/tmp.macro3
source map/temp/tmp.macro3
#
# 分類したデータのソート
#
cat map/temp/tmp02c | awk '{print $1;}' | sort | uniq | awk '{print "mkdir map/"$1;}' > map/temp/tmp.macro0a
source map/temp/tmp.macro0a
cat map/temp/tmp02c | awk '{print $1,$2;}' | sort | uniq | awk '{print "mkdir map/"$1"/"$2;}' > map/temp/tmp.macro1a
source map/temp/tmp.macro1a
cat map/temp/tmp02c | awk '{print $1,$2,$3;}' | sort | uniq | awk '{print "cat map/map_temp/"$1"/"$2"/"$3" | sort > map/"$1"/"$2"/"$3;}' > map/temp/tmp.macro4
